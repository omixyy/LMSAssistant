import json


class PromptBuilder:
    """
    Класс, составляющий промпт с жесткой структурой и четкими инструкциями
    """

    # Константы для системы оценивания
    GRADING_SCALE = {
        'excellent': {'min': 85, 'max': 100, 'desc': 'Отлично'},
        'good': {'min': 70, 'max': 84, 'desc': 'Хорошо'},
        'satisfactory': {'min': 50, 'max': 69, 'desc': 'Удовлетворительно'},
        'unsatisfactory': {'min': 0, 'max': 49, 'desc': 'Неудовлетворительно'}
    }

    def __init__(self, system_prompt: str = None):
        self.system_prompt = system_prompt or self._default_system_prompt()

    def build_prompt(self, task: str, answer: str, use_cot: bool = True) -> str:
        """
        Вернуть полный промпт
        """
        return (
            self._default_system_prompt() +
            f"""
            ОПИСАНИЕ РАБОТЫ:
            {task}

            ОТВЕТ СТУДЕНТА:
            {answer}
            """ +
            (self._build_cot_instructions() if use_cot else '') +
            self._build_response_format()
        )

    @staticmethod
    def _default_system_prompt() -> str:
        return """
        ТЫ - ЭКСПЕРТ-ПРЕПОДАВАТЕЛЬ, проверяющий студенческие работы.

        ТВОИ ОБЯЗАННОСТИ:
        1. Строго следовать инструкциям проверки
        2. Объективно оценивать работу по критериям
        3. Давать конструктивную обратную связь
        4. Отвечать ТОЛЬКО в указанном формате JSON
        
        ПРАВИЛА:
        - Не добавляй никакого текста вне JSON
        - Будь конкретным в замечаниях
        - Основывайся только на предоставленных материалах"""

    @staticmethod
    def _build_cot_instructions() -> str:
        """Инструкции Chain of Thought (без псевдографики)"""

        return """
        ИНСТРУКЦИИ ПО ПРОВЕРКЕ (ВЫПОЛНИ КАЖДЫЙ ШАГ ПО ПОРЯДКУ):
    
        === ШАГ 1: АНАЛИЗ УСЛОВИЯ ЗАДАНИЯ ===
        1.1. Прочитай условие задания полностью
        1.2. Выдели основные требования (списком)
        1.3. Определи ожидаемый результат
    
        === ШАГ 2: АНАЛИЗ РАБОТЫ СТУДЕНТА ===
        2.1. Определи, что сделал студент
        2.2. Найди все разделы работы
        2.3. Отметь использованные методы
    
        === ШАГ 3: СОПОСТАВЛЕНИЕ С ЗАДАНИЕМ ===
        3.1. Для каждого пункта задания проверь выполнение
        3.2. Отметь полностью выполненные пункты
        3.3. Отметь частично выполненные пункты
        3.4. Отметь невыполненные пункты
    
        === ШАГ 4: ПРОВЕРКА ФОРМУЛ (если есть) ===
        4.1. Проверь каждую формулу на корректность
        4.2. Проверь правильность применения
        4.3. Найди ошибки если есть
    
        === ШАГ 5: ПРОВЕРКА ГРАФИКОВ/ТАБЛИЦ (если есть) ===
        5.1. Проверь соответствие данным
        5.2. Проверь оформление (подписи осей, единицы)
        5.3. Найди ошибки если есть
    
        === ШАГ 6: ОЦЕНКА ПО КРИТЕРИЯМ ===
        6.1. Для каждого критерия из матрицы:
             - Выставь балл от 0 до max
             - Напиши обоснование
             - Укажи, что можно улучшить
    
        === ШАГ 7: ФОРМИРОВАНИЕ ОБРАТНОЙ СВЯЗИ ===
        7.1. Перечисли сильные стороны
        7.2. Перечисли основные недостатки
        7.3. Дай конкретные рекомендации
        7.4. Напиши общий вывод
    
        ВАЖНО: Каждый шаг должен найти отражение в соответствующем поле JSON.
        НЕ ПРОПУСКАЙ ШАГИ. Выполняй строго по порядку.
        """

    @staticmethod
    def _get_criteria_matrix() -> str:
        """Матрица критериев оценивания (чистый текст, без псевдографики)"""
        return """
        КРИТЕРИЙ 1: СООТВЕТСТВИЕ ТЕМАТИКЕ (0-10 баллов)
        ------------------------------------------------
        10 баллов - Полное соответствие теме и заданию
        7-9 баллов - Небольшие отклонения, но основная тема раскрыта
        4-6 баллов - Частичное соответствие, есть существенные отклонения
        1-3 балла - Слабое соответствие, тема не раскрыта
        0 баллов - Работа не соответствует теме
    
        КРИТЕРИЙ 2: ПОЛНОТА ВЫПОЛНЕНИЯ (0-25 баллов)
        ------------------------------------------------
        22-25 баллов - Выполнены ВСЕ пункты задания
        18-21 баллов - Выполнены основные пункты, есть мелкие недочеты
        12-17 баллов - Выполнена большая часть, но не все
        6-11 баллов - Выполнена меньшая часть задания
        0-5 баллов - Задание практически не выполнено
    
        КРИТЕРИЙ 3: КОРРЕКТНОСТЬ РАСЧЕТОВ/РЕШЕНИЙ (0-25 баллов)
        ------------------------------------------------
        22-25 баллов - Все расчеты верны, нет ошибок
        18-21 баллов - Незначительные ошибки, не влияющие на результат
        12-17 баллов - Есть ошибки, влияющие на некоторые результаты
        6-11 баллов - Множественные ошибки, результаты недостоверны
        0-5 баллов - Расчеты отсутствуют или полностью неверны
    
        КРИТЕРИЙ 4: КАЧЕСТВО АНАЛИЗА И ВЫВОДОВ (0-20 баллов)
        ------------------------------------------------
        18-20 баллов - Глубокий анализ, обоснованные выводы
        14-17 баллов - Хороший анализ, но выводы неполные
        9-13 баллов - Поверхностный анализ, выводы формальные
        4-8 баллов - Анализ практически отсутствует
        0-3 баллов - Нет анализа и выводов
    
        КРИТЕРИЙ 5: ОФОРМЛЕНИЕ (0-10 баллов)
        ------------------------------------------------
        9-10 баллов - Идеальное оформление, все требования соблюдены
        7-8 баллов - Хорошее оформление, мелкие недочеты
        4-6 баллов - Оформление с нарушениями
        1-3 баллов - Множественные нарушения оформления
        0 баллов - Оформление отсутствует
    
        КРИТЕРИЙ 6: СПИСОК ЛИТЕРАТУРЫ (0-10 баллов)
        ------------------------------------------------
        9-10 баллов - Корректное оформление, достаточное кол-во источников
        7-8 баллов - Небольшие ошибки в оформлении
        4-6 баллов - Ошибки в оформлении, мало источников
        1-3 баллов - Список литературы отсутствует или не по теме
        0 баллов - Нет списка литературы
        """

