from typing import Any


class PromptBuilder:
    """
    Класс, составляющий промпт
    """

    def __init__(self, system_prompt: str = None):
        self.system_prompt = system_prompt or self._default_system_prompt()

    @staticmethod
    def _default_system_prompt() -> str:
        return """
            Ты - опытный преподаватель высшего учебного заведения, проверяющий студенческие работы.
            Твоя задача - объективно оценить работу, дать конструктивную обратную связь и помочь студенту улучшить свои навыки.
    
            При проверке следуй этим принципам:
            1. Внимательно проанализируй условие задания, пойми, что требовалось сделать в работе, для себя выдели структуру задания.
            2. Внимательно проанализируй работу студента, пойми, что конкретно сделал студент, определи тематику работы студента.
            3. Оцени, насколько близки по тематике условие задания и работа студента
            4. Отмечай как сильные стороны, так и недостатки
            5. Давай конкретные рекомендации по улучшению
            """

    @staticmethod
    def _build_cot_instructions() -> str:
        """Инструкции с Chain of Thought - модель сама анализирует задание"""

        return """
            ИНСТРУКЦИИ ПО ПРОВЕРКЕ (следуй шагам):

            ШАГ 1 - Анализ условия задания:
            - Внимательно прочитай условие задания
            - Определи, что именно требовалось сделать
            - Выдели ключевые требования и ожидаемые результаты
            - Пойми, к какому типу относится задание (лабораторная, практическая, и т.д.)

            ШАГ 2 - Сопоставление с работой студента:
            - Проверь, соответствует ли работа условию задания
            - Определи, все ли требования выполнены
            - Выяви несоответствия и пропущенные элементы

            ШАГ 3 - Анализ содержания в контексте задания:
            - Оцени полноту раскрытия темы относительно задания
            - Проверь корректность использования терминов и методов
            - Проанализируй, насколько правильно решены поставленные задачи

            ШАГ 4 - Оценка по критериям:
            - Для каждого общего критерия определи, как он применим к данному заданию
            - Выставь баллы с обоснованием
            - Учитывай специфику задания

            ШАГ 5 - Формирование обратной связи:
            - Обобщи основные замечания в контексте задания
            - Выдели сильные стороны работы
            - Сформулируй рекомендации по улучшению
            """
    
    def build_prompt(self, task: str, answer: str, use_cot: bool = True) -> str:
        """
        Вернуть полный промпт
        """
        return (
            self._default_system_prompt() + 
            f"""
            ОПИСАНИЕ РАБОТЫ:
            {task}

            ОТВЕТ СТУДЕНТА:
            {answer}
            """ + (self._build_cot_instructions() if use_cot else '') +
            """
            В конце вкратце перескажи содержание работы студента и содержание описания работы
            """
        )
    